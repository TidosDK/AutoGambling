class Car {
    constructor(id, name, model, year, color, pricePerDay, isAvailable) {
        this.id = id;
        this.name = name;
        this.model = model;
        this.year = year;
        this.color = color;
        this.pricePerDay = pricePerDay;
        this.isAvailable = isAvailable;
    }
}

export default Car;
